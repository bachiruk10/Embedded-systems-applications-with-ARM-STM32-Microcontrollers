/*Callback function to receive message from Python client*/
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(recData == 'g'){
		  /*Message to start ADC received!, turn on the red led*/
		HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_SET);
		  /*Start the timer*/
		HAL_TIM_Base_Start(&htim8);
		  /*Start ADC with DMA*/
		  /* Start ADC sampling using DMA */
	    HAL_ADC_Start_DMA(&hadc1, (uint32_t *) pData, length);
	}

	else if (recData == 'r'){
		/*Turn off red led*/
		HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_RESET);
		/*Turn off red led*/
		HAL_GPIO_WritePin(GPIOG, GPIO_PIN_13, GPIO_PIN_RESET);
		/*Stop the timer*/
		HAL_TIM_Base_Stop(&htim8);
	}

	HAL_UART_Receive_IT(&huart1 , &recData , 1); // Need to put this to generate an interrupt for the next reception of byte
}
/*Callback function for data acquisition*/
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	/*Toggle green led for every ADC conversion*/
	HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_13);

	uint32_t average = 0;

    for (int i = 0; i < NCh; i++){
	for (int j = 0; j < length/NCh; j++){
		average += pData[j*NCh+i];
	}
	/*Digital value 0-4095*/
	ADC_Ave[i] = (float) (average / (length/NCh));
    /*Convert to voltage*/
    ADC_Ave_V [i] = (float) (Vref/pow(2,12))* ADC_Ave [i];
	average = 0;
    }


	/*Transmit data to client over UART*/
	HAL_UART_Transmit(&huart1, (uint8_t*)&counter, 2, 5000);

	HAL_UART_Transmit(&huart1, (uint8_t*)ADC_Ave_V, 12, 5000);

	counter++;
    /*In case counter get overflow */
	if (counter == pow(2,32)-1)
		counter = 0;
	__NOP();

}

